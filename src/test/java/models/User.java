package models;

public class User {
    private int id;
    private String name;
    private String surname;
    private String gender;
    private String country;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public String getGender() {
        return gender;
    }

    public String getCountry() {
        return country;
    }
}